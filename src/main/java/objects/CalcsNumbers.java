public class CalcsNumbers {
   

  private int calcs;
  private int loops;
  private int sleep;
  
   public int getCalcs() {
       return calcs;
   }

   public void setCalcs(int calcs) {
       this.calcs = calcs;
   }

   public int getLoops() {
       return loops;
   }

   public void setLoops(int loops) {
       this.loops = loops;
   }

   public int getSleep() {
       return sleep;
   }

   public void setSleep(int sleep) {
       this.sleep = sleep;
   }
   
}